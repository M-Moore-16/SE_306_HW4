# Addition A + B
def Add(A,B):
    C = A + B
    return C

# Subtraction A - B
def Sub(A,B):
    C = A - B
    return C

# Multiplication A * B
def Multi(A,B):
    C = A * B
    return C

# Division A / B
def Div(A,B):
    C = A / B
    return C

# Modulus
def Mod(A,B):
    C = A % B
    return C