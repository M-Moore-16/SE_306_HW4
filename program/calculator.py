# Addition A + B = C
def Add(A,B):
    C = A + B
    return C

# Subtraction A - B = C
def Sub(A,B):
    C = A - B
    return C

# Multiplication A * B = C
def Multi(A,B):
    C = A * B
    return C

# Division A / B = C
def Div(A,B):
    C = A / B
    return C

# Modulus A % B = C
def Mod(A,B):
    C = A % B
    return C